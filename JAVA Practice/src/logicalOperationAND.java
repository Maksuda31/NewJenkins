
public class logicalOperationAND {

	public static void main(String[] args) {
		
		// && = (AND) both conditions must be true
		int temp= 15;
		if (temp>30) { System.out.println("It's hot outside");}
		else if (temp>=20 && temp<=30) {System.out.println("It's warm outside");}
		else {System.out.println("It's cold outside");}
		
	}
	}

