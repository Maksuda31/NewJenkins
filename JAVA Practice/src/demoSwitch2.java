
public class demoSwitch2 {

	public static void main(String[] args) {
		String food = "Apple";
		
		switch (food)
		{
		case "Burger": System.out.println("It's a Burger");
		break;
		case "HotDog": System.out.println("It's a HotDog");
		break;
		case "Pizza": System.out.println("It's a Pizza");
		break;
		case "Sandwitch": System.out.println("It's a Sandwitch");
		break;
		case "Pastry": System.out.println("It's a Pastry");
		break;
		case "Cake": System.out.println("It's a Cake");
		break;
		default: System.out.println("It's not  a food");
		}

	}

}
