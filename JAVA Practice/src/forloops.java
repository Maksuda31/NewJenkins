
public class forloops {

	public static void main(String[] args) {
		//index, condition and increment/decrement
		for(int i=0; i<10; i++)
		{
			System.out.println(i);	
		}
		for (int i=10; i>=0; i--)
		{
			System.out.println("Happy New Year");
	
		}
		
		for (int i=10; i>=0; i-=8)
		{
			System.out.println("Happy New Year");
			
		}
	}

}
